package com.oshamahue.kotlindemo.conversion

class Conversion1SwitchCase {
    internal fun switchCase(i: Int) {
        when (i) {
            0 -> {
                print('k')
                print('o')
                print('t')
                print('l')
                print('i')
                print('n')
                print('\n')
                print('!')
            }
            1 -> {
                print('o')
                print('t')
                print('l')
                print('i')
                print('n')
                print('\n')
                print('!')
            }
            2 -> {
                print('t')
                print('l')
                print('i')
                print('n')
                print('\n')
                print('!')
            }
            3 -> {
                print('l')
                print('i')
                print('n')
                print('\n')
                print('!')
            }
            4 -> {
                print('i')
                print('n')
                print('\n')
                print('!')
            }
            5 -> {
                print('n')
                print('\n')
                print('!')
            }
            6 -> {
                print('!')
            }
            else -> print('\n')
        }
    }


}
