package com.oshamahue.kotlindemo.conversion;

public class Conversion1SwitchCase {
    void switchCase(int i) {
        switch (i) {
            case 0:
                System.out.print('k');
            case 1:
                System.out.print('o');
            case 2:
                System.out.print('t');
            case 3:
                System.out.print('l');
            case 4:
                System.out.print('i');
            case 5:
                System.out.print('n');
            default:
                System.out.print('\n');

        }
    }


}
